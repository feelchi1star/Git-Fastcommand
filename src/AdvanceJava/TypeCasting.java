package AdvanceJava;

public class TypeCasting {
    public static void main(String[] args) {
//    this is called explicit type casting

        long num1 = 1221L;
        int v = (int) num1;

        System.out.println(v);


//        this is called implicit type casting

double num2 = num1;
        System.out.println(num2);


    }
}
